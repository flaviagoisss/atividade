//Função - Function
//Como declarar
/* Funções -- Functions
 como declarar:
 >> deve ser declarada usando a palavara function seguida de "()" e "{}"
 */

 function nomedaFuncao() {
     console.log('menesagem de uma função');
 }

 // chamando a função
 nomedaFuncao();

 // função com parâmetro - definido parâmetros
 function mensagem(primeiro, segundo){
     console.log(primeiro, segundo);
 }

 // chamando a função e atribuindo valores aos parâmetros
 mensagem('Tudo certo','jovem!');